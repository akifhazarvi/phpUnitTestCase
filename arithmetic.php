<?php

namespace phpUnitTutorial;

class arithmetic
{
    public function add($a, $b)
    {
        $result= $a+$b;

        return $result;
    }
    public function minus($a, $b)
    {
        $result= $a-$b;

        return $result;
    }
    public function multiply($a, $b)
    {
        $result= $a*$b;

        return $result;
    }
    public function divide($a, $b)
    {
        $result= $a/$b;

        return $result;
    }
}



?>